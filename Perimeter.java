//Write a java program to find the area and perimeter of a rectangle using the arithmetic operators.
public class Perimeter 
{
	
	public static void main(String args[])
		{ 
		
//		 finding the square of  Perimeter & Area  
				int side= 2;
				System.out.println("Perimeter of Square: " + 4 * side);
				System.out.println("Area of Square: "+ side * side);
			
			
//			finding the Area of rectangle & perimeter of rectangle
				double len = 6;
				double breadth = 4;
				System.out.println("Perimeter of Rectangle: "+ 2*(len+breadth));
				System.out.println("Area of Rectangle: "+ len*breadth);
				
		}
	
}


