//Write a java program to demonstrate all assingment ope,(==,+=,-=,*=,/=,%=)
public class AssignmetOpeDemon 
{
public static void main(String args[])
	{
//	Declare the value 
		int a = 22;
		int b = 4;
		int c = 3;
		int d = 22;
		int e = 10;
		
		// perform the assignment ope 

		a+=5; 
		b-=2;
		c*=3;
		d/=11;
		e%=4;
//		print the statements
		System.out.println("a+ " + a);
		System.out.println("b- " + b);
		System.out.println("c* " + c);
		System.out.println("d/ " + d);
		System.out.println("e% " + e);

		
		
	}
}
