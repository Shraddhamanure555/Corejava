//Write a java program to Increment by 5 using += & Decrement by 3 using -=
public class IncreDecremetnt 
{
public static void main (String args[])
	{
	//	perform the increment operation 
			int increment = 5;
			increment+=5;
			System.out.println("Increment by 5: "+ increment);
	//	perform the decrement operation
			int decrement = 8;
			decrement-=3;
			System.out.println("Decrement by 3: "+ decrement);
		
	}
}
// Output: Increment by 5: 10 .... Decrement by 3: 5
